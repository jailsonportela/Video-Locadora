    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nome=txtNome.getText();
        String login=txtLogin.getText();
        String senha=txtSenha.getText();
        if(nome.equals("")||login.equals("")||senha.equals("")){
             JOptionPane.showMessageDialog(null,"N√£o pode haver campos vazios",
                    "Video Locadora",JOptionPane.ERROR_MESSAGE);
        }else{
          Funcionario f=new Funcionario();
          f.setNome(nome);
          f.setLogin(login);
          f.setSenha(senha);
          FuncionarioDAO sql=new FuncionarioDAO();
          if(sql.cadastrarFuncionario(f)){
                    JOptionPane.showMessageDialog(null,"Funcion√°rio cadastrado",
                    "Video Locadora",JOptionPane.INFORMATION_MESSAGE);
          }else{
                    JOptionPane.showMessageDialog(null,"Erro ao cadastrar funcion√°rio",
                    "Video Locadora",JOptionPane.ERROR_MESSAGE);
          }
          Conexao.fecharConexao(sql.getCon());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

